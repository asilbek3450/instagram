// Flag images for each language
const langFlags = {
    en: 'https://flagcdn.com/w20/gb.png',
    uz: 'https://flagcdn.com/w20/uz.png',
    ru: 'https://flagcdn.com/w20/ru.png'
};
const langLabels = { en: 'EN', uz: "UZ", ru: 'RU' };

function changeLanguage(lang) {
    localStorage.setItem("app_lang", lang);
    applyLanguage(lang);

    // Update base.html sidebar lang button
    const langBtnText = document.getElementById("lang-btn-text");
    if (langBtnText) langBtnText.innerText = lang.toUpperCase();

    // Update flag-based switcher (landing / downloader / login / register)
    const flagImg = document.getElementById("lang-flag-img");
    const flagLabel = document.getElementById("lang-flag-label");
    if (flagImg) flagImg.src = langFlags[lang] || langFlags.en;
    if (flagLabel) flagLabel.innerText = langLabels[lang] || 'EN';
}

function applyLanguage(lang) {
    const dict = translations[lang] || translations['en'];
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (dict[key] !== undefined) {
            if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
                el.placeholder = dict[key];
            } else {
                el.innerText = dict[key];
            }
        }
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        const key = el.getAttribute("data-i18n-placeholder");
        if (dict[key] !== undefined) el.placeholder = dict[key];
    });
}

document.addEventListener("DOMContentLoaded", () => {
    const savedLang = localStorage.getItem("app_lang") || "en";
    changeLanguage(savedLang);
});
